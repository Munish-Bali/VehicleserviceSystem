package com.coforge.JASproject.JEW_AUTO_SYSTEM.bean;

public class Jewellery {

	private int jewId,jewKarat;
	private String jewName ,jewType ;
	private float jewWeight ,jewPrice ;
	private String jewDesigner ;
	public int getJewId() {
		return jewId;
	}
	public void setJewId(int jewId) {
		this.jewId = jewId;
	}
	public int getJewKarat() {
		return jewKarat;
	}
	public void setJewKarat(int jewKarat) {
		this.jewKarat = jewKarat;
	}
	public String getJewName() {
		return jewName;
	}
	public void setJewName(String jewName) {
		this.jewName = jewName;
	}
	public String getJewType() {
		return jewType;
	}
	public void setJewType(String jewType) {
		this.jewType = jewType;
	}
	public float getJewWeight() {
		return jewWeight;
	}
	public void setJewWeight(float jewWeight) {
		this.jewWeight = jewWeight;
	}
	public float getJewPrice() {
		return jewPrice;
	}
	public void setJewPrice(float jewPrice) {
		this.jewPrice = jewPrice;
	}
	public String getJewDesigner() {
		return jewDesigner;
	}
	public void setJewDesigner(String jewDesigner) {
		this.jewDesigner = jewDesigner;
	}
	
}
